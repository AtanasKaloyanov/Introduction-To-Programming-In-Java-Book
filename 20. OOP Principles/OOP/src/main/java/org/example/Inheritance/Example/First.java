package org.example.Inheritance.Example;

public class First {
    private String firstFirst;
    private String firstSecond;

    public First() {
        
    }

    public First(String firstFirst, String firstSecond) {
        this.firstFirst = firstFirst;
        this.firstSecond = firstSecond;
    }

    public String getFirstFirst() {
        return firstFirst;
    }

    public String getFirstSecond() {
        return firstSecond;
    }
}
