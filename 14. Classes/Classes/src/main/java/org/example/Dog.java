package org.example;

public class Dog {
     private String name;
     private int age;
     public Dog(String name, int age) {
          this.name = name;
          this.age = age;
     }

     public Dog() {

     }

     public String getName() {
          return this.name;
     }

     public int getAge() {
          return this.age;
     }
}


